package Training;

public class Ticket
{
	private double ticketNumber;
	private String ticketDescription;
	private int ticketStatus;
	private String ticketType;
	private String ticketResolution;
	public String getTicketResolution() {
		return ticketResolution;
	}
	public void setTicketResolution(String ticketResolution) {
		this.ticketResolution = ticketResolution;
	}
	public String getTicketType() {
		return ticketType;
	}
	public void setTicketType(String ticketType) {
		this.ticketType = ticketType;
	}
	public double getTicketNumber() {
		return ticketNumber;
	}
	public void setTicketNumber(double ticketNumber) {
		this.ticketNumber = ticketNumber;
	}
	public String getTicketDescription() {
		return ticketDescription;
	}
	public void setTicketDescription(String ticketDescription) {
		this.ticketDescription = ticketDescription;
	}
	public int getTicketStatus() {
		return ticketStatus;
	}
	public void setTicketStatus(int ticketStatus) {
		this.ticketStatus = ticketStatus;
	}

	
	
}

